    /** Индекс интервала (0..12) для номинального размера в мм; -1 если вне диапазона. */
    fun rangeIndexFor(sizeMm: Double): Int {
        // Верхняя граница включительно; для 0 попадаем в 0-3.
        return ranges.indexOfFirst { sizeMm > it.from && sizeMm <= it.to }
            .let { if (it == -1 && sizeMm == 0.0) 0 else it }
    }

    /** Значение допуска IT в мкм. Возвращает null для несуществующих размеров/грейдов. */
    fun itMicrons(sizeMm: Double, gradeIndex: Int): Double? {
        val ri = rangeIndexFor(sizeMm)
        if (ri < 0 || gradeIndex !in table[0].indices) return null
        return table[ri][gradeIndex]
    }

    fun itMicrons(sizeMm: Double, grade: String): Double? {
        val gi = grades.firstOrNull { it.code == grade }?.index ?: return null
        return itMicrons(sizeMm, gi)
    }

    data class Range(val from: Double, val to: Double) {
        val gmean: Double get() = kotlin.math.sqrt(from * to)
    }

