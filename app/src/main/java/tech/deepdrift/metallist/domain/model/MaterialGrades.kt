package tech.deepdrift.metallist.domain.model

/**
 * Популярные марки материалов для подсказок в поле «Марка».
 *
 * Марка — необязательное поле, влияет только на текст в истории и заявке.
 * Плотность для расчёта берётся из выбранного материала (грубая категория),
 * поскольку внутри одной группы плотности близки (± 1-2 %).
 *
 * Ключ — точное имя материала из `MaterialsSeed`. Если материал отсутствует
 * в этой карте, подсказок не будет, но свободный ввод остаётся доступен.
 */
object MaterialGrades {

    val byMaterial: Map<String, List<String>> = mapOf(
        "Сталь" to listOf("Ст3", "Сталь 20", "Сталь 45", "40Х", "09Г2С", "У8", "Р6М5"),
        "Нержавеющая сталь" to listOf("12Х18Н10Т", "08Х18Н10", "10Х17Н13М2Т", "AISI 304", "AISI 316", "AISI 430"),
        "Алюминий" to listOf("АД0", "АД1", "АД31", "АМц", "АМг3", "АМг6"),
        "Дюралюминий" to listOf("Д16", "Д16Т", "Д1", "В95"),
        "Медь" to listOf("М1", "М2", "М3"),
        "Латунь" to listOf("Л63", "Л68", "ЛС59-1"),
        "Бронза" to listOf("БрОЦС5-5-5", "БрАЖ9-4", "БрБ2"),
        "Титан" to listOf("ВТ1-0", "ВТ6", "ОТ4"),
        "Чугун" to listOf("СЧ15", "СЧ20", "ВЧ40", "ВЧ50"),
        "Никель" to listOf("НП1", "НП2"),
        "Нихром" to listOf("Х20Н80", "Х15Н60"),
    )

    /** Список подсказок для указанного материала (может быть пустым). */
    fun suggestionsFor(materialName: String?): List<String> =
        byMaterial[materialName].orEmpty()
}
